const nome = "Rayato";
const peso = "60";
const altura = "1.70";

const imc = (peso / (altura * altura));

if (imc >= 30) {

    console.log (`${nome} voce está acima do peso.`)

} else {

    console.log (`${nome} voce não está acima do peso.`)

}





